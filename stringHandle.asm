.MODEL
.STACK
.DATA
X DB 80 DUP(?) 
;MSG DB "$"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV SI,0
MOV AH,1
NEXT: INT 21H

CMP AL,0DH
JZ EXIT
MOV X[SI],AL
INC SI
JMP NEXT
EXIT:
 
 ;ENTER NEW LINE
MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H

 ;STRING REVERSE
DEC SI
MOV AH,2
NEXT1: 


MOV DL,X[SI] 
INT 21H
CMP SI,0
JZ EXIT1
DEC SI
JMP NEXT1
EXIT1:

MOV AH,4CH
MAIN ENDP
END MAIN 

