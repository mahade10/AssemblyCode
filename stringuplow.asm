.MODEL
.STACK
.DATA
X DB 80 DUP(?)
.CODE
MOV AX,@DATA
MOV DS,AX
MAIN PROC

MOV SI,0
MOV AH,1
NEXT:INT 21H
CMP AL,0DH
JZ EXIT
MOV X[SI],AL
INC SI
JMP NEXT
EXIT:
  
  
 ;ENTER NEW LINE
MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H

;STRING REVERSE
DEC SI


MOV AL,X[SI]

CMP AL,'z' 
JG UCH
CMP AL,'A' 
JL UCH
   

CMP AL,95
JL SCASE
SUB AL,64

SCASE:
ADD AL,32 

UCH:
MOV DL,AL
MOV AH,2
INT 21H

MOV AH,2
NEXT1:  
INT 21H
CMP SI,0
JZ EXIT1
DEC SI
JMP NEXT1
EXIT1: 

MOV AH,4CH
MAIN ENDP
END MAIN  