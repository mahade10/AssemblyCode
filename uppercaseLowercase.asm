.MODEL
.STACK
.DATA
.CODE
MAIN PROC
MOV AH,1
INT 21H
MOV BL,AL

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H
MOV AL,BL  

CMP AL,'z' 
JG UCH
CMP AL,'A' 
JL UCH
   

CMP AL,95
JL SCASE
SUB AL,64

SCASE:
ADD AL,32 

UCH:
MOV DL,AL
MOV AH,2
INT 21H

MOV AH,4CH
MAIN ENDP
END MAIN    